package Clases;

/**
 *
 * @author Toshiba
 */
public class Dimensional {

    private int idDimensional;
    private String dimensional;

    public Dimensional() {
        this.idDimensional = idDimensional;
        this.dimensional = dimensional;
    }

    public Dimensional(int idDimensional, String dimensional) {
        this.idDimensional = idDimensional;
        this.dimensional = dimensional;
    }

    public int getIdDimensional() {
        return idDimensional;
    }

    public void setIdDimensional(int idDimensional) {
        this.idDimensional = idDimensional;
    }

    public String getDimensional() {
        return dimensional;
    }

    public void setDimensional(String dimensional) {
        this.dimensional = dimensional;
    }

}
